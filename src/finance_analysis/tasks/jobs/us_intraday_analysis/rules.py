@dataclass(frozen=True)
class _ConditionResult:
    key: str
    label: str
    matched: bool


def _condition(key: str, matched: bool, label: str) -> _ConditionResult:
    return _ConditionResult(key=key, label=label, matched=bool(matched))


def _candidate(
    signal_type: str,
    score: float,
    scored: Sequence[_ConditionResult],
    failures: Sequence[str],
    strong: bool,
) -> Candidate:
    return {
        "signal_type": signal_type,
        "score": score,
        "max_score": float(len(scored)),
        "matched_conditions": [item.label for item in scored if item.matched],
        "failed_conditions": [item.label for item in scored if not item.matched],
        "failed_condition_keys": list(failures),
        "rule_version": RULE_VERSION,
        "severity": "high" if strong else "medium",
        "rule_strength": "strong" if strong else "normal",
    }


DEFAULT_INTRADAY_SIGNAL_RULES: List[SignalRule] = [
    SignalRule("relative_strength_breakout", evaluate_relative_strength_breakout),
    SignalRule("weak_to_strong_reversal", evaluate_weak_to_strong_reversal),
    SignalRule("relative_strength_failure", evaluate_relative_strength_failure),
]


def _gte(value: Any, threshold: float) -> bool:
    parsed = safe_float(value)
    return bool(parsed is not None and parsed >= threshold)


def _lte(value: Any, threshold: float) -> bool:
    parsed = safe_float(value)
    return bool(parsed is not None and parsed <= threshold)
