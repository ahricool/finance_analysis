def is_relative_strength_breakout(metrics: Metrics) -> bool:
    strong = RELATIVE_STRENGTH_BREAKOUT_RULE["strong"]
    return (
        _gte(metrics.get("change_5m"), strong["change_5m_min"])
        and _gte(metrics.get("change_15m"), strong["change_15m_min"])
        and _gte(metrics.get("relative_to_qqq_15m"), strong["relative_to_qqq_15m_min"])
        and _gte(metrics.get("volume_ratio_5m"), strong["volume_ratio_5m_min"])
        and bool(metrics.get("price_above_vwap"))
        and (
            bool(metrics.get("near_intraday_high"))
            or _lte(metrics.get("high_distance_pct"), strong["near_high_pct"])
        )
    )


